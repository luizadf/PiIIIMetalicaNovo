    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        
        int codigo = 0;
        if(!"".equals(txtCodigo.getText())){
            codigo = Integer.parseInt(txtCodigo.getText());
        }

        Empresa novaEmpresa = new Empresa();
        
        novaEmpresa.NovaEmpresa(codigo, txtOperadora.getText(), txtCNPJ.getText(), txtEndereco.getText());
        
        ControleEmpresa cadastrarEmpresa = new ControleEmpresa();
        
        if(cadastrarEmpresa.Nova(novaEmpresa)){
            JOptionPane.showMessageDialog(null,"Cadastrado com susseso!");
            txtCodigo.setText(Integer.toString(Integer.parseInt(txtCodigo.getText())+1));
            txtOperadora.setText("");
            txtCNPJ.setText("");
            txtEndereco.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Favor digitar todos os campos");
        }
        
    }//GEN-LAST:event_btCadastrarActionPerformed


